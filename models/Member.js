const mongoose = require('mongoose');




const _member = new mongoose.Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    phone: {
        type: String,
        required: true,
    }
})


const Member = mongoose.model('Member', _member)
module.exports = Member